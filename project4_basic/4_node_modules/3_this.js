function hello() {
  console.log('✔️ this in function scope');
  console.log(this);
}
hello(); // global

class A {
  constructor(num) {
    this.num = num;
  }
  memberFunction() {
    console.log('✔️ this in class method scope');
    console.log(this);
  }
}
const a = new A(1);
a.memberFunction(); // A { num: 1 }

console.log('✔️ this in global scope');
console.log(this); // {}, not global object : vs 브라우저에서의 global scope에서의 this는 window object
console.log(this === module.exports); // true → module.exports?? <뒤에서 설명>

