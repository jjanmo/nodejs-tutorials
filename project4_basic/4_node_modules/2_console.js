// log level
console.log('log'); // 개발
console.info('info'); // 정보
console.warn('warn'); // 경보
console.error('error'); // 에러 → 사용자에러, 시스템에러

// assert
console.assert(2 === 3, 'not same!');
console.assert(2 === 2, 'same!');

// print object
const student = { name: 'ellie', age: 20, company: { name: 'AC' } };
console.log(student);
console.table(student);
/**
 * options
 * - showHidden : object's non-enumerable and symbol properties 까지 보여줄 것인가
 * - depth : 중첩된 객체를 어디까지 보여줄 것인가
 */
console.dir(student, { showHidden: false, colors: true, depth: 1 });

console.time('for loop'); // 같은 레이블 설정
for (let i = 0; i < 1000; i++) {
  i++;
}
console.timeEnd('for loop');

